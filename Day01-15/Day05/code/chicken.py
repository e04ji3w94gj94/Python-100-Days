"""
求解《百錢百雞》問題
1隻公雞5元 1隻母雞3元 3隻小雞1元 用100元買100隻雞
問公雞 母雞 小雞各有多少隻

Version: 0.1
Author: 駱昊
Date: 2018-03-02
"""

for x in range(0, 20):
    for y in range(0, 33):
        z = 100 - x - y
        if 5 * x + 3 * y + z / 3 == 100:
            print('公雞: %d隻, 母雞: %d隻, 小雞: %d隻' % (x, y, z))
            
